print(1 > 2)  # Mayor que con resultado False
print(1 < 2)  # Menor que con resultado True
print(1 <= 2)  # Menor o igual que con resultado True
print(1 >= 2)  # mayor o igual que con resultado False
print(2 >= 2)  # Mayor o igual que con resultado True
print(2 <= 2)  # Menor o igual que con resultado True
print(2 == 2)  # Igual a con resultado True
print(2 == 3)  # Igual a con resultado False
print(2 == "2")  # Igual a con resultado False (Int y string no es lo mismo)
print(2 != "2")  # No es igual a con resultado True
print(2 != 2)  # No es igual a con resultado False
