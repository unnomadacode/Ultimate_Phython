# Variales numéricas
numero = 2  # entero o integer
decimal = 1.2  # float
imaginario = 2 + 2j  # j sería el número imaginario

# Operaciones
print(1 + 2)  # Suma
print(1 - 3)  # Resta
print(1 * 3)  # Multiplicación
print(1 / 3)  # Divisón con decimales
print(1 // 3)  # División sin decimales
print(8 % 3)  # Módulo (Resto de la divisón)
print(2 ** 2)  # Potencias

# Remplazando el valor de una variable
numero += 2  # Se le suma a la variable el número de la derecha
numero -= 2  # Se le resta a la variable el número de la derecha
numero *= 2  # Se multiplica la variable por el número de la derecha
numero /= 2  # Se divide la variable entre el número de la derecha
print("numero", numero)
