# Secuencias de escape

# Backslash "\": Se emplea para decirle a python que justamente lo que se encuentra a la
# derecha del backslash no es un caracter propio del lenguaje sino que lo utilice dentro del string
# Si queremos mostrar un backslash debemos usar doble backslash
curso = "Ultimate \"Python\""
print(curso)

# \n se puede emplear para hacer un salto de linea en un mismo string
curso = "Ultimate \nPython"
print(curso)
